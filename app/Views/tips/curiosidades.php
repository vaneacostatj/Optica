                        #7 Muchas personas usando lentillas<br>
Un dato muy curioso con respecto a las lentillas y que no puedes dejar de saber es que, en la nación europea de España, más de 2.5 millones de personas hacen uso de las lentillas, por lo que no es de extrañar que haya un buen mercado.<br><br>

#8 Lentillas para tus mascotas<br>
¿Sabías que tus adorables mascotas pueden hacer uso de lentillas? Si antes tenían problemas de cataratas, con las lentillas intraoculares parecidas a las que tus usas podrán corregir este problema. Ayuda a eliminar el problema, recupera la visión y podrás recuperar la relación entre animal y dueño.<br>
A su vez, los científicos creadores de estas maravillas están en busca de ampliar el rango para la acción, de este modo pueden obtener lentillas para corregir las cataratas en los caballos.<br>

#9 Los lentes de contacto desechable<br>
Para el año de 1982, se crearon las primeras lentillas de carácter desechable, es decir, que se pueden utilizar solo una vez. Estos cuentan con la característica de mejorar el PMMA que contienen las lentes rígidas, ya que, permiten que haya una mayor cantidad de oxigenación.<br>

Esto puede prevenir edemas de córnea que eran frecuentes en las lentillas duras. El único problema de este tipo de lentillas es que se deforman mucho más rápido.<br><br>
