                        #3 Las primeras lentillas en cristal<br>
Las primeras lentillas fueron fabricadas por F.A. Müller, un alemán que se especializaba en las prótesis oculares. Su prototipo estaba hecho de cristal transparente de este modo se hace la protección del globo ocular.<br>
<br>
#4 Se pueden usar con cualquier problema y edad<br>
Las lentillas cuentan con la propiedad de poder ser usadas a cualquier edad, y se encuentran totalmente indicadas para muchos casos de visión, puesto que ofrecen mayor visión periférica y amplitud en la oculomotricidad.<br>
<br>
#5 Zeiss la primera firma de lentillas<br>
En el mercado optometrista, la firma comercial Zeiss fue la primera en hacer la presentación de las lentillas en el mercado.
<br><br>
#6 Son permeables a gas<br>
Las lentillas que poseen una constitución entre rígida y blanda, son permeables ante el gas, además, son las que cuentan con una duración de unos tres a seis meses. No obstante, son lo más sencillos para hacer los cuidados.<br><br>


