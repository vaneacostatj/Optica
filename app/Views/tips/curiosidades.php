#1 Idea original de Leonardo Da Vinci<br>
Para el año 1508, el gran genio y artista de todos los tiempos Leonardo da Vinci, tenía en consideración que el ojo humano eran el órgano de mayor importancia en el cuerpo humano.<br>
Es así, como entre sus escritos añadió un dibujo sobre una especie de sistema óptico. Éste consistía en una semiesfera hecha en vidrio, que estaba llena de agua y contenía un rostro sumergido en el mismo. Además, realizó otro dibujo de unas lentillas muy parecidas a las que se ven en la actualidad.<br>
<br>
#2 Lentes ortoqueratológicos<br>
Hay una regla básica de mantenimiento para las lentillas que implica el no dormir con ellas, no obstante, existen los lentes ortoqueratológicas u Orto-K, las cuales se pueden utilizar durante la noche.<br>
<br>
Éstas cuentan con la habilidad de corregir de forma temporal la miopía, mientras, en el día no es necesario que hagas uso de las lentillas ni las gafas. Además, son de gran eficacia cuando se trata de tener control sobre el avance de la misma miopía, es por ello por lo que forma parte de los tratamientos para ello.<br>




