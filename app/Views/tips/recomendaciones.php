                    <div class="card-body">
                    
                        <p class="card-text">
                        1.- Evitar un acercamiento excesivo en las tareas de cerca (al leer, escribir, dibujar o al usar el móvil u ordenador) ya que trastorna el sistema acomodativo, oculomotor y/o binocular.<br>

                        2.- La distancia de los ojos a la lectura, escritura, al móvil o a la tablet debe ser de unos 35cm (la distancia que hay del codo a la palma de la mano).<br>

                        3.- La distancia de los ojos a los ordenadores portátiles debe ser algo mayor (de 40 a 45cm) y a los ordenadores de sobremesa de 45 a 55cm (la distancia que hay del hombro a los dedos de la mano).<br>
                        </p>
                    
                    </div>
