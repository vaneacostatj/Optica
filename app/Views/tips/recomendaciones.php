                    <div class="card-body">
                    
                        <p class="card-text">
                        7.- Realizar las tareas escolares en una zona ordenada en un espacio abierto, amplio y con ventanas porque la luz natural siempre es más recomendable que la artificial pero procurando que la mesa no esté directamente enfrente de ellas para evitar deslumbramientos.
                        <br>
                        8.- Al menos cada 30 minutos de estudio, deberes o videojuegos se debe descansar 5 minutos relajando la visión de cerca y mirando de lejos. Es lo que los profesionales sanitarios de la visión llamamos la regla 20-20-20 (relajar la vista 20 segundos cada 20 minutos, mirando hacia algo que esté al menos a 20 pies).<br>

                        9.- Además de la luz general de la habitación, se debe usar otra de refuerzo en la mesa de estudio en la zona contraria a su lateralidad para no provocar sombras (izquierda si es diestro y derecha si es zurdo) pero evitando que deslumbre directamente en los ojos.<br>

                        10.- Evitar ver la TV a una distancia inferior a 2-3 metros y siempre con luz ambiental. Nunca con la habitación a oscuras que anula la visión periférica.<br>
                        </p>
