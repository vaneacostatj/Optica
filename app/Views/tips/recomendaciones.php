                        <p class="card-text">
                        4.- Además, a la hora de estudiar con ordenadores de sobremesa:<br>
                        a) La pantalla debe ser configurada con valores de luminosidad y contraste adecuados.<br>
                        b) La zona superior de la pantalla debe estar a la altura o ligeramente inferior a la de los ojos.<br>
                        c) Evitar el deslumbramiento por tener una fuente de luz o ventana tras la pantalla.<br>

                        5.- Hay que estar sentado adecuadamente, a una altura suficiente con respecto a la mesa, con la espalda recta (a ser posible en ángulo de 90°) y con los pies tocando el suelo.<br>

                        6.- Evitar posturas raras prolongadas que conlleven el tener la espalda o la cabeza girada o doblada al estudiar, usar videojuegos o ver la TV.<br>
                        </p>
                        
