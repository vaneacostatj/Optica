                        <p class="card-text">                 

                        A continuación proporcionamos una serie de consejos para promover una buena salud visual infantil. Con estas recomendaciones estaremos evitando o retrasando la aparición de disfunciones visuales y síntomas asociados  frecuentes como visión cercana borrosa, dolores de cabeza, ojos secos o irritados, dolor de cuello y espalda o sensibilidad a la luz, entre otros.
