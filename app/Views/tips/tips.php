Todas aquellas personas que utilizan gafas saben lo que es tener que cuidarlas diariamente. Por esta razón, muchas veces escuchamos esta pregunta en nuestras ópticas, ¿cómo puedo cuidar mis lentes progresivas? En Essilor nos preocupamos por ti, para que mantengas tus gafas progresivas bien cuidadas ya que sabemos lo complicado que puede ser.<br><br>

Trucos para cuidar mis lentes progresivas<br>
Mantener en perfecto estado tus gafas puede ser una labor tediosa, e incluso complicada. Desde Essilor queremos ofrecerte unos sencillos trucos que te ayudarán a mantener tus lentes progresivas en óptimas condiciones durante más tiempo. Asimismo, sentirás que tus gafas están en mejor estado y que su cuidado diario es mucho más sencillo. Tenemos así los siguientes consejos.<br><br>

Utilizar la gamuza<br>
Limpia, a diario, las lentes de tus gafas progresivas con la gamuza que te proporciona tu óptico-optometrista. Otros sistemas, por suaves que parezcan, pueden rayar la superficie. Recuerda, una zona sucia en una lente es una zona traslucida y no transparente. Además, esto supone una pérdida de la calidad visual en las lentes de tus gafas.<br>
