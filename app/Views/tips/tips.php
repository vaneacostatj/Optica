                        Evita dejar huellas<br>
Sujeta siempre las gafas por la montura, así evitaras ensuciar las lentes y dejar residuos que puedan ser abrasivos, en las mismas. Los tratamientos anti-reflejantes te ayudarán a que tus lentes permanezcan limpias por más tiempo.<br><br>

Utiliza la funda<br>
Cuando no estés usando las gafas déjalas siempre dentro de su estuche, donde estarán más protegidas a cualquier percance exterior.<br><br>

Ajústalas con el tiempo<br>
Acude a tu óptico-optometrista como mínimo cada 6 meses para que las revise y las regule adecuadamente a tus necesidades.   
