Pliega tus gafas<br>
Al quitarte las gafas, lo idóneo es dejarlas plegadas, de este modo es más difícil que se rompan por cualquier accidente.<br><br>

Ten en cuenta el lavado<br>
Cuando cuentas con una montura metálica en tus lentes progresivas debes evitar mojarlas en exceso. No obstante, durante el proceso de lavado es importante utilizar agua y jabón neutros. Al mismo tiempo, se recomienda secarlas con un paño fino de algodón para finalizar el lavado.<br><br>

Evitar los productos abrasivos<br>
En el mercado existen gran variedad de productos específicos para la limpieza de las gafas. No obstante, es importante tener en cuenta que se debe evitar el uso de productos abrasivos.<br><br>

Cuidado con las fuentes de calor<br>
No coloques tus gafas progresivas cerca de una fuente de calor, o en el interior del coche a pleno sol. Esto se debe a que podrían sufrir daños importantes, tanto la montura como las lentes.<br><br>

Cuida las varillas<br>
Utiliza siempre las dos manos para quitarte las gafas, ya que así se minimiza el riesgo de deformar las varillas.
